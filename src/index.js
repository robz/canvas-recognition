function predict(model, data) {
  return Array.from(model.predict(tf.tensor2d(data, [1, 784])).dataSync());
}

class Canvas {
  constructor(id, onChange) {
    this.canvas = document.getElementById('canvas');
    this.onChange = onChange;
    this.canvas.onmousedown = this.onMouseDown.bind(this);
    this.canvas.onmouseup = this.onMouseUp.bind(this);
    this.canvas.onmousemove = this.onMouseMove.bind(this);
    this.ctx = this.canvas.getContext('2d');
    this.down = false;
    this.clearData();
  }

  clearData() {
    this.data = Array(28 * 28).fill(0);
    this.draw(this.data);
  }

  draw(data) {
    this.ctx.fillStyle = '#ffffff';
    this.ctx.fillRect(0, 0, 500, 500);
    this.ctx.fillStyle = '#000000';
    for (let r = 0; r < 28; r++) {
      for (let c = 0; c < 28; c++) {
        if (data[r * 28 + c] > 0) {
          this.ctx.fillRect((500 / 28) * c, (500 / 28) * r, 500 / 28, 500 / 28);
        }
      }
    }
  }

  onMouseDown(e) {
    this.down = true;
    this.clearData();
  }

  onMouseMove(e) {
    if (!this.down) {
      return;
    }
    const x = e.clientX - this.canvas.offsetLeft;
    const y = e.clientY - this.canvas.offsetTop;
    const r = Math.round(y / (500 / 28));
    const c = Math.round(x / (500 / 28));
    // TODO: why is this necessary
    if (r >= 0 && r < 28 && c >= 0 && c < 28) {
      this.data[r * 28 + c] = 1;
      if (r > 0) {
        this.data[(r - 1) * 28 + c] = 1;
      }
      if (c > 0) {
        this.data[r * 28 + (c - 1)] = 1;
      }
    }
    this.draw(this.data);
    this.onChange(this.data);
  }

  onMouseUp(e) {
    this.down = false;
  }
}

function writePrediction(prediction) {
  prediction
    .map(e => Math.round(e * 100) / 100)
    .forEach(
      (v, i) => (document.getElementById('prediction' + i).innerHTML = v),
    );
}

async function main() {
  const model = await tf.loadLayersModel('/model/model.json', false);
  writePrediction(await predict(model, arr));

  let id;
  const canvas = new Canvas('canvas', data => {
    clearTimeout(id);
    // debounce for better responsiveness when drawing
    id = setTimeout(async () => writePrediction(await predict(model, data)));
  });
  canvas.draw(arr);

  for (let i = 0; i < 10; i++) {
    document.getElementById('button' + i).onclick = () => {
      console.log('button ' + i + '!!!');
    };
  }
}

main();
